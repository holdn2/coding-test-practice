# 거스름돈 문제
# 최적의 해를 빠르게 구하기 위해서는 가장 큰 화폐 단위부터 돈을 거슬러 준다.
# 가장 큰 화폐 단위부터 돈을 거슬러 주는 것이 최적의 해를 보장하는 이유는?
# => 가지고 있는 동전 중에서 큰 단위가 항상 작은 단위의 배수!!이므로 작은 단위의 동전들을 종합하여 다른 해가 나올 수 없기 때문에

# 만약 800원을 거슬러 줄 때, 화폐 단위가 500, 400, 100 이라면? => 그리디로 풀 수 없다.

# 거스름돈 예시
n = 1260
count = 0
# 큰 단위의 화폐부터 차례대로 확인하기
array = [500, 100, 50, 10]
for coin in array:
    count += n // coin  # 해당 화폐로 거슬러 줄 수 있는 동전의 개수 세기
    n %= coin
print(count)

# 거스름돈 시간복잡도 : 화폐의 종류가 K라고 할 때, O(K). 거스름돈의 금액과는 무관하며, 동전의 총 종류에만 영향을 받는다.

