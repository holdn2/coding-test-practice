# 모험가 길드 문제
# 한 마을에 모험가가 N명 있다. 모험가 길드에서는 N명의 모험가를 대상으로 '공포도'를 측정했는데, '공포도'가 높은 모험가는 쉽게 공포를 느껴 위험 상황에서 제대로 대처할 능력이 떨어진다.
# 모험가 길드장은 모험가 그룹을 안전하게 구성하고자 공포도가 X인 모험가는 반드시 X명 이상으로 구성한 모험가 그룹에 참여해야 여행을 떠날 수 있도록 규정했다.
# 최대 몇 개의 모험가 그룹을 만들 수 있는지 그룹 수의 최댓값을 구해라.
# 몇 명의 모험가는 마을에 그대로 남아있어도 되므로 모든 모험가를 특정한 그룹에 넣을 필요는 없다.

# 내가 예상한 답 : 풀이 실패...
# n = int(input())
# data = list(map(int, input().split()))

# 문제 풀이
# 오름차순 정렬 이후 공포도가 낮은 모험가부터 하나씩 확인한다.
# 앞에서부터 공포도를 하나씩 확인하며 '현재 그룹에 포함된 모험가의 수'가 '현재 확인하고 있는 공포도'보다 크거나 같다면 이를 그룹으로 지정한다.
# 공포도가 오름차순으로 정렬되어 있어서 항상 최소한의 모험가의 수만 포함하여 그룹이 결성된다.
n = int(input())
data = list(map(int, input().split()))
data.sort()  # 오름차순 정렬
result = 0  # 총 그룹 수
count = 0  # 현재 그룹에 포함된 모험가의 수

for i in data:  # 공포도를 낮은 것부터 하나씩 확인
    count += 1  # 현재 그룹에 해당 모험가를 포함시키기
    if (
        count >= i
    ):  # 현재 그룹에 포함된 모험가의 수가 현재의 공포도 이상이라면, 그룹 결성
        result += 1  # 총 그룹 수 1 증가
        count = 0  # 현재 그룹에 포함된 모험가 수 초기화
print(result)
