# 조건문에서 실행될 소스코드가 한 줄인 경우, 줄바꿈 하지 않고도 가략하게 표현할 수 있다.

# 조건부 표현식 : if ~ else문을 한 줄에 작성할 수 있다.
score = 85
result = "Success" if score >= 80 else "Fail"
print(result)

print("-------------------------------")

